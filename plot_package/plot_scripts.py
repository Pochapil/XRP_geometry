import matplotlib.pyplot as plt
import numpy as np
import scienceplots

import config

plt.style.use(['science', 'notebook', 'grid'])

import matplotlib as mpl

mpl.rcParams['mathtext.fontset'] = 'cm'
mpl.rcParams['font.family'] = 'STIXGeneral'


def extend_arr_for_plot(arr):
    # нужно расширить массивы, чтобы покрыть фазу [0,2]
    append_index = config.N_phase_for_plot - config.N_phase
    array_to_plot = np.append(arr, arr[0:append_index])
    return array_to_plot


def plot_L(L):
    legend_labels_arr = [r'$top_{pol}$', r'$top_{eq}$', r'$bottom_{pol}$', r'$bottom_{eq}$', 'sum']

    buf = L.copy()
    L[0] = buf[1]
    L[1] = buf[0]
    L[2] = buf[3]
    L[3] = buf[2]

    colors_arr = ['red', 'red', 'green', 'green']
    marker_arr = ['.', '*', '+', '^']
    linestyle_arr = [':', '-']

    fig, ax = plt.subplot_mosaic('a', figsize=(12, 6))

    phi_for_plot = np.linspace(0, 2, config.N_phase_for_plot)

    for i, surf_L in enumerate(L):
        ax['a'].plot(phi_for_plot, extend_arr_for_plot(surf_L), label=legend_labels_arr[i], color=colors_arr[i],
                     marker=marker_arr[i], linestyle=linestyle_arr[i % 2], markersize=12)

    ax['a'].plot(phi_for_plot, extend_arr_for_plot(np.sum(L, axis=0)), label=legend_labels_arr[-1], color='black')

    x_axis_label = r'$\Phi$'
    y_axis_label = r'$L_{\rm{iso}}$' + ' [erg/s]'
    ax['a'].set_xlabel(x_axis_label, fontsize=24)
    ax['a'].set_ylabel(y_axis_label, fontsize=24)
    ax['a'].legend()
    plt.show()
    # ax['a'].set_ylabel('')
